struct Node
{
   int data;
   int dist;
   Node* left;
   Node* right;
};

int max_val=INT_MIN;
int min_val=INT_MAX;

// Utility function to create a new Tree Node
Node* newNode(int val,int dist)
{
   Node* temp = new Node;
   temp->dist=dist;
   if(dist<min_val) min_val=dist;
   if(dist>max_val) max_val=dist;
   temp->data = val;
   temp->left = NULL;
   temp->right = NULL;

   return temp;
}


// Function to Build Tree
Node* buildTree(string str)
{
   // Corner Case
   if(str.length() == 0 || str[0] == 'N')
       return NULL;

   // Creating vector of strings from input
   // string after spliting by space
   vector<string> ip;

   istringstream iss(str);
   for(string str; iss >> str; )
       ip.push_back(str);

   // Create the root of the tree
   Node* root = newNode(stoi(ip[0]),0);

   // Push the root to the queue
   queue<Node*> queue;
   queue.push(root);

   // Starting from the second element
   int i = 1;
   while(!queue.empty() && i < ip.size()) {

       // Get and remove the front of the queue
       Node* currNode = queue.front();
       queue.pop();

       // Get the current node's value from the string
       string currVal = ip[i];

       // If the left child is not null
       if(currVal != "N") {

           // Create the left child for the current node
           currNode->left = newNode(stoi(currVal),currNode->dist-1);

           // Push it to the queue
           queue.push(currNode->left);
       }

       // For the right child
       i++;
       if(i >= ip.size())
           break;
       currVal = ip[i];

       // If the right child is not null
       if(currVal != "N") {

           // Create the right child for the current node
           currNode->right = newNode(stoi(currVal),currNode->dist+1);

           // Push it to the queue
           queue.push(currNode->right);
       }
       i++;
   }

   return root;
}

void inorder(Node *root, vector<int> &v)
{
    if(root==NULL)
        return;

    inorder(root->left, v);
    v.push_back(root->data);
    inorder(root->right, v);
}

void inorderTraverse(Node *node){
	if(!node) return ;

	inorderTraverse(node->left);
	cout<<node->data<<" "<<node->dist<<endl;
	inorderTraverse(node->right);
}


